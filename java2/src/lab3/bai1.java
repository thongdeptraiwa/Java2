    private void btnSXTenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSXTenActionPerformed
       //Collections.sort(list, (sv1, sv2) -> (sv1.getName().compareTo(sv2.getName())));
        Comparator<dataBai2> comp = new Comparator<dataBai2>() {
            @Override
            public int compare(dataBai2 o1, dataBai2 o2) {
                return (o1.getName().compareTo(o2.getName()));
            }
        };
        Collections.sort(list, comp);
       fillToTable();
    }//GEN-LAST:event_btnSXTenActionPerformed

