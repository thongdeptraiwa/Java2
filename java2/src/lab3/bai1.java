    public void initNganh(){
        String[] nganh = new String[]{"Web","Mobile","Phát Triển Phần Mềm"};
        DefaultComboBoxModel<String> boxModel = new DefaultComboBoxModel<>(nganh);
        cbxNganh.setModel(boxModel);
    }
    public void initData(){
        list.add(new dataBai2("SV1","Mobile",9));
        list.add(new dataBai2("SV2","Web",6));
        list.add(new dataBai2("SV3","Phát Triển Phần Mềm",3.4));
    }
    public void initTable(){
        tblModel = (DefaultTableModel) tblDanhSach.getModel();
        String[] cols = new String[] {"Họ Và Tên","Điểm","Ngành","Học Lực","Thưởng"};
        tblModel.setColumnIdentifiers(cols);
    }
    public void fillToTable(){
        tblModel.setRowCount(0);
        for(dataBai2 sv: list ){
            Object[] row = new Object[]{sv.getName(),sv.getDiem(),sv.getNganh(),sv.getHocLuc(),sv.isThuong()};
            tblModel.addRow(row);
        }
    }
    public void showDetail(){
        int index = tblDanhSach.getSelectedRow();
        txtName.setText(list.get(index).getName());
        txtDiem.setText(String.valueOf(list.get(index).getDiem()));
        cbxNganh.setSelectedItem(list.get(index).getNganh());
        txtHocLuc.setText(list.get(index).getHocLuc());
        chkThuong.setSelected(list.get(index).isThuong());
        
    }
