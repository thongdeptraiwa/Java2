    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
       if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Chua nhap ho va ten!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(new String(txtDiem.getText()).equals("")){
            JOptionPane.showMessageDialog(this, "Chua nhap diem!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        for(dataBai2 i: list){
            if(i.getName().equals(txtName.getText())){
                int choice = (JOptionPane.showConfirmDialog(this, "Update ?", "confirm", JOptionPane.YES_NO_OPTION));
                
                if(choice == JOptionPane.YES_OPTION){
                    try {
                        i.setDiem(Double.parseDouble(txtDiem.getText()));
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Nhap sai định dạng điểm!", "Error", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                    
                    fillToTable();
                    JOptionPane.showMessageDialog(this, "da cap nhat !");
                    btnNhapMoiActionPerformed(evt);
                    return;
                }else{
                    
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "nguoi dung khong ton tai");
    }//GEN-LAST:event_btnCapNhatActionPerformed

