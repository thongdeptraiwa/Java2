    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Chua nhap ho va ten!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //xoa
        for(dataBai2 i: list){
            if(i.getName().equals(txtName.getText())){
                int choice = (JOptionPane.showConfirmDialog(this, "Detele ?", "confirm", JOptionPane.YES_NO_OPTION));
                
                if(choice == JOptionPane.YES_OPTION){
                    list.remove(i);
                    fillToTable();
                    JOptionPane.showMessageDialog(this, "da xoa");
                    btnNhapMoiActionPerformed(evt);
                    return;
                }else{
                    
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Sinh Vien khong ton tai");
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
         //thong bao chua nhap
        if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Chua nhap ho va ten!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(new String(txtDiem.getText()).equals("")){
            JOptionPane.showMessageDialog(this, "Chua nhap diem!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        dataBai2 sv = new dataBai2();
        sv.setName(txtName.getText());   
        //thong bao loi diem
        try {
            sv.setDiem(Double.parseDouble(txtDiem.getText()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Nhap sai định dạng điểm!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        sv.setNganh((String) cbxNganh.getSelectedItem());
        txtHocLuc.setText(sv.getHocLuc());
        chkThuong.setSelected(sv.isThuong());
        
        list.add(sv);
        fillToTable();
        JOptionPane.showMessageDialog(this, "Da add thanh cong!");
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_btnThemActionPerformed

